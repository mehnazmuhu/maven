public class Firoza {

	//create a method which return even number from 1 to 20
	public void printEvenNumbers() {
	    for (int i = 2; i <= 20; i += 2) {
	        System.out.println(i);
	    }
	}
	
	//create a method which return odd number from 1to 30
	public void printOddNumbers() {
	    for (int i = 1; i <= 30; i += 2) {
	        System.out.println(i);
	    }
	}
	}
	

